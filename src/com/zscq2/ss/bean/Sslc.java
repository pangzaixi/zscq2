package com.zscq2.ss.bean;

import java.util.Date;

/**
 * 诉讼流程
 * @author thinker
 *
 */
public class Sslc {
	 int id;//` int(11) NOT NULL AUTO_INCREMENT,
	 String ajh;//` varchar(255) DEFAULT NULL COMMENT '案件号',
	 Date createdate;//` date DEFAULT NULL COMMENT '操作日期',
	 String context;//` varchar(255) DEFAULT NULL COMMENT '操作内容',
	 String creater;//` varchar(255) DEFAULT NULL COMMENT '操作人',
	 String qt;//` varchar(255) DEFAULT NULL COMMENT '其他，备注',
	 int pid;//` int(11) DEFAULT NULL COMMENT '父记录id',
	 
	 
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getAjh() {
		return ajh;
	}
	public void setAjh(String ajh) {
		this.ajh = ajh;
	}
	public Date getCreatedate() {
		return createdate;
	}
	public void setCreatedate(Date createdate) {
		this.createdate = createdate;
	}
	public String getContext() {
		return context;
	}
	public void setContext(String context) {
		this.context = context;
	}
	public String getCreater() {
		return creater;
	}
	public void setCreater(String creater) {
		this.creater = creater;
	}
	public String getQt() {
		return qt;
	}
	public void setQt(String qt) {
		this.qt = qt;
	}
	public int getPid() {
		return pid;
	}
	public void setPid(int pid) {
		this.pid = pid;
	}
	 
	 
}
